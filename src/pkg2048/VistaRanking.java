    private void escriureRank(String user){
        try {
            ArrayList<TuplePair<String,Integer>> rank = CPJugar.consultarRanking();
            Vector<String> jugadors = new Vector<>();
            Vector<String> puntuacions = new Vector<>();
            for (TuplePair<String,Integer> tuple : rank) {
                jugadors.add(tuple.getL());
                puntuacions.add(""+tuple.getR());
                if(tuple.getL().equals(user)) txtPuntuacio.setText(""+tuple.getR());
            }
            listJugador.setListData(jugadors);
            listPuntuacio.setListData(puntuacions);
            
        } catch (Exception ex) {
            lblError.setText(ex.getMessage());
        }
    }

